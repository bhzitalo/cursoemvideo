# Exercício 006: Crie um algoritmo que leia um número e mostre o seu dobro, triplo e raiz quadrada.

# Recebe um número do usuário.
numero = int(input("Digite um número: "))

# Exibe o número digitado, o dobro, triplo e raiz quadrada.
print(f"Você digitou o número {numero}")
print(f"Seu dobro é {numero + numero}")
print(f"Seu triplo é {numero * 3}")
print(f"Sua raiz quadrada é {numero ** 0.5}")